package week1.Assgn;

public class CheckIfTheNumberIsPositiveOrNegative {

	public static void main(String[] args) {
		
		int number = 0;
		
		if(number > 0)
		{
			System.out.println("The number " + number + " is postive number");
		}
		else if(number < 0)
		{
			System.out.println("The number " + number + " is negative number");
		}
		else
		{
			System.out.println("The number " + number + " is neither postive nor negative number");
		}

	}

}
